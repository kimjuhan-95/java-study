/**
 * 문제 5. 산술/논리 연산
 * 
 * int a = 7;
 * int b = 3;
 * System.out.println(a + b);
 * System.out.println(a / b);
 * System.out.println(a % b);
 * System.out.println(a > b && b > 0);
 * System.out.println(!(a == 7));
 * 
 * 예상 출력 결과:
 * 
 * 1)10
 * 2)2
 * 3)1
 * 4)True
 * 5)False
 * 5번문제는 애매함 확실히는 모르겟음
 */

public class Sol {

	public static void main(String[] args) {
		int a = 7;
		  int b = 3;
		  System.out.println(a + b);
		  System.out.println(a / b);
		  System.out.println(a % b);
		  System.out.println(a > b && b > 0);
		  System.out.println(!(a == 7));
	}

}
